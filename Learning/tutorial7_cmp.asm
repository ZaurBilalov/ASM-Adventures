	; CMP - compares two numbers
	; we can use:
		jb			; jump if below
		jbe			; jump if below or equal
		ja			; jump if above
		jae			; jump if above or equal
		
	; to compare signed numbers:
		jl			; jump if lower
		jle			; jump if lower or equal
		jg			; jump if greater
		jge			; jump if greater or equal