	; x86 Processor

	; Real mode - every CPU start in this mode - can access to less than 1MB of code and data
	; Protected mode - Since Intel's 80286 CPU - Can access 1GB in 16bit and more in the future releases - have memory protection
	; We will learn to program the Protected mode
	; Long mode - Since AMD's Opteron CPU - Can handle data of 64bits
	; And there are more modes.
	; We are going to learn from the end - Long and Protected first, Real last.